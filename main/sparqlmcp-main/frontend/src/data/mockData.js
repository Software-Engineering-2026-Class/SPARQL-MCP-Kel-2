// ── SPARQL Query Generator ─────────────────────────────────

/**
 * Generates a mock SPARQL query string based on the user's
 * natural language input. In production, this would be
 * generated by the AI/MCP agent backend.
 *
 * @param {string} query - Natural language search query
 * @returns {string} Mock SPARQL query
 */
export function mockSparqlQuery(query) {
  return `# Auto-generated SPARQL Query
# Input: "${query}"
# Generated by: Federated Agentic SPARQL Engine

PREFIX cyber: <https://w3id.org/sepses/vocab/ref/cve#>
PREFIX malware: <https://w3id.org/sepses/vocab/ref/malware#>
PREFIX rdfs: <http://www.w3.org/2000/01/rdf-schema#>
PREFIX owl: <http://www.w3.org/2002/07/owl#>

SELECT DISTINCT ?entity ?label ?type ?cvss ?vector ?firstSeen
WHERE {
  # Match malware entities
  {
    ?entity a malware:MalwareFamily ;
            rdfs:label ?label ;
            malware:firstSeen ?firstSeen ;
            malware:primaryVector ?vector .
    BIND("malware" AS ?type)
  }
  UNION
  # Match CVE vulnerabilities
  {
    ?entity a cyber:CVE ;
            rdfs:label ?label ;
            cyber:cvssScore ?cvss ;
            cyber:affectedProduct ?target .
    BIND("vulnerability" AS ?type)
    OPTIONAL { ?entity cyber:vector ?vector }
  }

  # Filter by relevance to user query
  FILTER(
    CONTAINS(LCASE(STR(?label)), "emotet") ||
    CONTAINS(LCASE(STR(?label)), "cve-2023")
  )
}
ORDER BY DESC(?cvss)
LIMIT 20`
}

